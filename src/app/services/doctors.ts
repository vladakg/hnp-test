////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

import { Injectable } from '@angular/core';
import { doctors } from '../../mockups/doctors';
import { HttpClient } from '@angular/common/http';

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

@Injectable()
export class DoctorsService {

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    constructor(private http: HttpClient) {}

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    getDoctors() {
        return doctors;

        // in production
        // return this.http.get('http://some-api/getDoctors');
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    getDoctorName(id: number) {
        const index = doctors.findIndex(i => i.id === id);
        return doctors[index].firstName + ' ' + doctors[index].lastName;
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
}
